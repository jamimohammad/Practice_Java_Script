//3.26 (عملگرهای && ، || و ^) برنامه‌ای در جاوااسکریپت بنویسید که یک عدد از کاربر بگیرد و بررسی کند آیا بر 5 و 6 بخش‌پذیر است، بر یکی از آن‌ها یا دقیقاً بر یکی از آن‌ها.

var num = Number(prompt('enter a number'))

alert((num % 5 === 0) && (num % 6 === 0))
alert((num % 5 === 0) || (num % 6 === 0))
alert((num % 5 === 0) ^ (num % 6 === 0))


