// /3.34 (هندسه: نقطه روی قطعه‌خط؟)
// برنامه‌ای در جاوااسکریپت بنویسید که سه نقطه بگیرد و بررسی کند آیا نقطه سوم روی قطعه‌خط بین دو نقطه اول قرار دارد یا نه.


//3.29 (هندسه: دو دایره)
// برنامه‌ای در جاوااسکریپت بنویسید که مرکز و شعاع دو دایره را بگیرد و بررسی کند آیا دایره دوم داخل اولی است، با آن همپوشانی دارد یا جداست.


//3.13 (کاربرد مالی: محاسبه مالیات)
// برنامه‌ای در جاوااسکریپت بنویسید که نرخ مالیات را برای وضعیت‌های مختلف (single, married …) محاسبه کند.