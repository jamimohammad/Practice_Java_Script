// می‌توان از قانون کرامر (Cramer’s Rule) برای حل دستگاه معادلات خطی 2 در 2 استفاده کرد:
// ax + by = e
// cx + dy = f
// فرمول‌ها:
// x = (ed - bf) / (ad - bc)
// y = (af - ec) / (ad - bc)
// یک برنامه جاوااسکریپت بنویسید که دستگاه زیر را حل کند و مقدار x و y را نمایش دهد:
// 3.4x + 50.2y = 44.5
// 2.1x + 0.55y = 5.9


//3.1 (Algebra: حل معادله درجه دوم) برنامه‌ای در جاوااسکریپت بنویسید که از کاربر سه مقدار a, b, c بگیرد و ریشه‌های معادله درجه دوم ax² + bx + c = 0 را محاسبه و نمایش دهد. اگر دترمینان مثبت باشد دو ریشه واقعی نمایش داده شود، اگر صفر باشد یک ریشه، و اگر منفی باشد پیغام بدهد "The equation has no real roots".




//3.3 (Algebra: حل دستگاه معادلات خطی 2×2) برنامه‌ای در جاوااسکریپت بنویسید که مقادیر a, b, c, d, e, f را از کاربر بگیرد و دستگاه معادلات خطی 2×2 را با قانون کرامر حل کند. اگر ad - bc = 0 بود پیام بدهد که "The equation has no solution".




//3.7 (کاربرد مالی: واحدهای پولی) برنامه‌ای در جاوااسکریپت بنویسید که مقدار پول (به سنت) بگیرد و فقط مقادیر غیرصفر را به شکل درست نمایش دهد. برای یک واحد از حالت مفرد (1 dollar, 1 penny) و برای چند واحد از حالت جمع استفاده کند.




